def slug_generator(size, chars=string.ascii_lowercase + string.ascii_uppercase + string.digits):
    return ''.join(random.choice(chars) for _ in range(size))


class Command(BaseCommand):
    def handle(self, *args, **options):
        products_count = len(Product.objects.all())

        if products_count == 0:
            products = []
            products_images = []
            status = ProductStatus.objects.get(pk=1)

            for i in range(1, 51):
                while True:
                    slug = slug_generator(size=6)
                    if not Product.objects.filter(slug=slug).exists() and not ProductImage.objects.filter(
                            slug=slug).exists():
                        break
                price = str(i) + "000"
                product = Product(name=junk_names[i], description=junk_descriptions[i], price=int(price), slug=slug,
                                  status=status)

                image = f'products/samples/{random.choice(["1", "2", "3", "4", "5", "6", "7", "8"])}.jpg'
                products_image = ProductImage(product=product, slug=slug, image=image)
                products_images.append(products_image)
                products.append(product)

            if len(products) != 0:
                Product.objects.bulk_create(products)
                ProductImage.objects.bulk_create(products_images)
                print("50 products added !")

        else:
            print("There are some products in database !")
